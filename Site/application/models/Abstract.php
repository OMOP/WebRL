class Application_Model_Abstract
{
    public function __construct(array $options = null)
    {
        if (is_array($options)) {
            $this->setOptions($options);
        }
    }

    public function __set($name, $value)
    {
        $method = 'set' . $name;
        if (('mapper' == $name) || !method_exists($this, $method)) {
            throw new Exception('Invalid method property');
        }
        $this->$method($value);
    }

    public function __get($name)
    {
        $method = 'get' . $name;
        if (('mapper' == $name) || !method_exists($this, $method)) {
            throw new Exception('Invalid method property');
        }
        return $this->$method();
    }

    public function setOptions(array $options)
    {
        $methods = get_class_methods($this);
        foreach ($options as $key => $value) {
            $method = 'set' . ucfirst($key);
            if (in_array($method, $methods)) {
                $this->$method($value);
            }
        }
        return $this;
    }

    public function toArray()
    {
        
        $result = array();
        
        foreach(get_class_methods(get_class($this)) as $method) {
            if (preg_match('/^get/', $method) && $method != 'getMapper') {
                $keyName = substr($method, 3);
                $keyName[0] = strtolower($keyName[0]);
                $result[$keyName] = call_user_func(array($this, $method));
            }
        }
        return $result;
    } 

}